#!/usr/bin/env node

import { execSync } from 'node:child_process';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';

const ROOT = process.cwd();
const OUTPUT_DIR = path.join(ROOT, 'prototype');
const OUTPUT_FILE = path.join(OUTPUT_DIR, 'combined-prototype.tsx');
const MANIFEST_FILE = path.join(OUTPUT_DIR, 'prototype-manifest.json');

const SCRIPT_EXTENSIONS = new Set(['.ts', '.tsx', '.js', '.jsx', '.mjs', '.cjs']);
const EXCLUDES = new Set([
  'prototype/combined-prototype.tsx',
  'prototype/prototype-manifest.json',
]);

function getGitTrackedFiles() {
  const output = execSync('git ls-files', { encoding: 'utf8' });
  return output
    .split('\n')
    .map((file) => file.trim())
    .filter(Boolean)
    .filter((file) => !EXCLUDES.has(file))
    .filter((file) => SCRIPT_EXTENSIONS.has(path.extname(file)));
}

function resolveImport(fromFile, importTarget, candidates) {
  if (!importTarget.startsWith('.')) return null;

  const fromDir = path.dirname(fromFile);
  const normalized = path.normalize(path.join(fromDir, importTarget)).replace(/\\/g, '/');

  const options = [
    normalized,
    ...Array.from(SCRIPT_EXTENSIONS, (ext) => `${normalized}${ext}`),
    ...Array.from(SCRIPT_EXTENSIONS, (ext) => `${normalized}/index${ext}`),
  ];

  return options.find((option) => candidates.has(option)) ?? null;
}

function buildDependencyGraph(files) {
  const fileSet = new Set(files);
  const importPattern = /(?:import|export)\s+(?:[^'";]+?\s+from\s+)?['"]([^'"]+)['"]/g;
  const graph = new Map(files.map((file) => [file, new Set()]));

  for (const file of files) {
    const source = readFileSync(path.join(ROOT, file), 'utf8');
    let match;

    while ((match = importPattern.exec(source)) !== null) {
      const resolved = resolveImport(file, match[1], fileSet);
      if (resolved) {
        graph.get(file).add(resolved);
      }
    }
  }

  return graph;
}

function topologicalSort(files, graph) {
  const visiting = new Set();
  const visited = new Set();
  const ordered = [];

  function visit(file) {
    if (visited.has(file)) return;
    if (visiting.has(file)) return; // Ignore circular edges and continue.

    visiting.add(file);

    const dependencies = Array.from(graph.get(file) ?? []).sort();
    for (const dependency of dependencies) {
      visit(dependency);
    }

    visiting.delete(file);
    visited.add(file);
    ordered.push(file);
  }

  for (const file of [...files].sort()) {
    visit(file);
  }

  return ordered;
}

function buildPrototypeBundle(orderedFiles) {
  const header = [
    '/**',
    ' * AUTO-GENERATED PROTOTYPE BUNDLE',
    ' * Generated by scripts/build-prototype.mjs',
    ` * Generated at: ${new Date().toISOString()}`,
    ' */',
    '',
  ];

  const chunks = orderedFiles.flatMap((file) => {
    const source = readFileSync(path.join(ROOT, file), 'utf8').trimEnd();
    return [
      `// ----- START: ${file} -----`,
      source,
      `// ----- END: ${file} -----`,
      '',
    ];
  });

  return [...header, ...chunks].join('\n');
}

function run() {
  const files = getGitTrackedFiles();

  if (files.length === 0) {
    throw new Error('No tracked script files were found to build a prototype bundle.');
  }

  const graph = buildDependencyGraph(files);
  const orderedFiles = topologicalSort(files, graph);

  mkdirSync(OUTPUT_DIR, { recursive: true });

  const bundle = buildPrototypeBundle(orderedFiles);
  writeFileSync(OUTPUT_FILE, bundle, 'utf8');

  const manifest = {
    generatedAt: new Date().toISOString(),
    totalFiles: orderedFiles.length,
    files: orderedFiles,
  };
  writeFileSync(MANIFEST_FILE, `${JSON.stringify(manifest, null, 2)}\n`, 'utf8');

  console.log(`Prototype bundle written to ${path.relative(ROOT, OUTPUT_FILE)}`);
  console.log(`Manifest written to ${path.relative(ROOT, MANIFEST_FILE)}`);
  console.log(`Files included: ${orderedFiles.length}`);
}

run();
